def get_number_word(num: int) -> str:
    return NUMBER_WORDS.get(num, str(num))

def generate_captcha() -> tuple[str, int]:
    a = random.randint(1, 15)
    b = random.randint(1, 15)
    
    word_a = get_number_word(a)
    word_b = get_number_word(b)
    
    question = f"{word_a} додати {word_b}"
    answer = a + b
    
    return question, answer
